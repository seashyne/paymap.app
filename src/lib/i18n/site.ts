export type SiteLang = "en" | "th" | "lo" | "zh" | "ja" | "ko" | "vi" | "es" | "fr" | "de"

const SITE_LANG_COOKIE = "paymap-lang"

export function normalizeLang(value?: string | null): SiteLang | null {
  if (!value) return null
  const lang = value.toLowerCase()
  if (lang.startsWith("th")) return "th"
  if (lang.startsWith("lo") || lang.startsWith("la")) return "lo"
  if (lang.startsWith("zh")) return "zh"
  if (lang.startsWith("ja")) return "ja"
  if (lang.startsWith("ko")) return "ko"
  if (lang.startsWith("vi")) return "vi"
  if (lang.startsWith("es")) return "es"
  if (lang.startsWith("fr")) return "fr"
  if (lang.startsWith("de")) return "de"
  if (lang.startsWith("en")) return "en"
  return null
}

export const supportedSiteLangs: SiteLang[] = ["en", "th", "lo", "zh", "ja", "ko", "vi", "es", "fr", "de"]

